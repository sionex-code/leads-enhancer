// Colorful brand chips for whatever social links a lead has. Falls back to a
// dash when there are none. Used in tables, cards and lead drawers alike.
export function Socials({ lead }) {
  const present = SOCIALS.filter((s) => lead[s.key]);
  if (!present.length) return <span className="text-xs text-muted-foreground">-</span>;
  return (
    <div className="flex flex-wrap gap-1">
      {present.map((s) => (
        <a
          key={s.key}
          href={lead[s.key]}
          target="_blank"
          rel="noreferrer"
          title={s.label}
          aria-label={s.label}
          onClick={(e) => e.stopPropagation()}
          className="inline-flex h-5 w-5 items-center justify-center rounded-md text-white shadow-sm transition hover:opacity-80"
          style={{ background: s.bg }}
        >
          <svg viewBox="0 0 24 24" width="12" height="12" fill="currentColor" aria-hidden="true">
            <path d={s.path} />
          </svg>
        </a>
      ))}
    </div>
  );
}

// Unmistakable WhatsApp registration badge: green check when on WhatsApp, red
// X when not, nothing when never checked. Returns null so call sites can render
// it inline next to a phone without an empty box.
export function WaIcon({ lead, size = 14, className = "" }) {
  const st = waState(lead);
  if (st === "yes") {
    return (
      <MessageCircleCheck
        size={size}
        className={cn("shrink-0 text-emerald-600", className)}
        title={lead.whatsapp_id || lead.whatsappId || "On WhatsApp"}
      />
    );
  }
  if (st === "no") {
    return <MessageCircleX size={size} className={cn("shrink-0 text-red-600", className)} title="Not on WhatsApp" />;
  }
  return null;
}

// A lead's phone, rendered as a click-to-chat wa.me link (green) when the number
// is on WhatsApp and a link can be built, otherwise as plain text. Renders null
// when the lead has no phone so call sites can supply their own placeholder.
export function WaPhone({ lead, className = "" }) {
  if (!lead?.phone) return null;
  const link = waState(lead) === "yes" ? waMeLink(lead) : "";
  if (link) {
    return (
      <a
        href={link}
        target="_blank"
        rel="noreferrer"
        onClick={(e) => e.stopPropagation()}
        title="Chat on WhatsApp"
        className={cn("text-emerald-600 hover:underline", className)}
      >
        {lead.phone}
      </a>
    );
  }
  return <span className={className}>{lead.phone}</span>;
}
